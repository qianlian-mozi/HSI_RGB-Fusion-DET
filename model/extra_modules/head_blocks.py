class GSConv(nn.Module):
    # GSConv https://github.com/AlanLi1997/slim-neck-by-gsconv
    def __init__(self, c1, c2, k=1, s=1, p=None, g=1, d=1, act=True):
        super().__init__()
        c_ = c2 // 2 # 将输出特征图的通道数对半分
        self.cv1 = Conv(c1, c_, k, s, p, g, d, Conv.default_act)  # Conv集成了2DCONV+BN
        self.cv2 = Conv(c_, c_, 5, 1, p, c_, d, Conv.default_act) # group设置为了C_，符合了DSC的理念

    def forward(self, x):
        x1 = self.cv1(x) # 出来1/2大小的特征（进行了下采样）
        x2 = torch.cat((x1, self.cv2(x1)), 1) # 进行DSC并且进行res性质的cat
        # shuffle 
        # y = x2.reshape(x2.shape[0], 2, x2.shape[1] // 2, x2.shape[2], x2.shape[3])
        # y = y.permute(0, 2, 1, 3, 4)
        # return y.reshape(y.shape[0], -1, y.shape[3], y.shape[4])

        b, n, h, w = x2.size()
        b_n = b * n // 2
        y = x2.reshape(b_n, 2, h * w) # 将通道分为两组（每组n//2个通道），并将空间维度展平
        y = y.permute(1, 0, 2) # [2, b_n, h*w]，将“组”维度（原通道维度的分组）移动到了最前面。
        y = y.reshape(2, -1, n // 2, h, w)
        ## 这部分混合了不同层的特征，促进信息的流动，类似于ShuffleNet等网络中的“通道混洗”（channel shuffle）操作。
        ## 但实现方式有所不同。最终，这个函数返回了经过变换和重排后的特征图。
        
        return torch.cat((y[0], y[1]), 1)

class GSBottleneck(nn.Module):
    # GS Bottleneck https://github.com/AlanLi1997/slim-neck-by-gsconv
    def __init__(self, c1, c2, k=3, s=1, e=0.5):
        super().__init__()
        c_ = int(c2*e)
        # for lighting
        self.conv_lighting = nn.Sequential(
            GSConv(c1, c_, 1, 1), # 先调整用GSC（下采样）成一半
            GSConv(c_, c2, 3, 1, act=False)) # 再用GSC（上采样）成想要的输出大小
        self.shortcut = Conv(c1, c2, 1, 1, act=False)

    def forward(self, x):
        return self.conv_lighting(x) + self.shortcut(x)

